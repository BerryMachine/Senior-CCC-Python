#This is pretty straight forward
N = int(input())

num_of_s = 0
num_of_t = 0

for i in range(N):
  #spliting the string letter by letter
  line = [*input()]

  #cheking throught all the letters
  for letter in line:
    if letter == "T" or letter == "t":
      num_of_t += 1
    if letter == 'S' or letter == "s":
      num_of_s += 1

#ending if statement for the numerical values
if num_of_t > num_of_s:
  print("English")
else: print("French")
  
